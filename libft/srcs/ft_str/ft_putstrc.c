#include "libft.h"

void	ft_putstrc(char const *s, unsigned int c)
{
	int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0' && s[i] != (unsigned char)c)
		{
			write(1, &s[i], 1);
			i++;
		}
	}
}
